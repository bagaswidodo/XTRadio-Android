public class JadwalAcaraItem {
    String namaAcara;
    String keteranganAcara;
    String startAcara;
    String finishAcara;
    String pj;

    public JadwalAcaraItem(){

    }

    public String getNamaAcara() {
        return namaAcara;
    }

    public void setNamaAcara(String namaAcara) {
        this.namaAcara = namaAcara;
    }

    public String getKeteranganAcara() {
        return keteranganAcara;
    }

    public void setKeteranganAcara(String keteranganAcara) {
        this.keteranganAcara = keteranganAcara;
    }

    public String getStartAcara() {
        return startAcara;
    }

    public void setStartAcara(String startAcara) {
        this.startAcara = startAcara;
    }

    public String getFinishAcara() {
        return finishAcara;
    }

    public void setFinishAcara(String finishAcara) {
        this.finishAcara = finishAcara;
    }

    public String getPj() {
        return pj;
    }

    public void setPj(String pj) {
        this.pj = pj;
    }
}
